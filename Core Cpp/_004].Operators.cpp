#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int a = 10, b = 2 ;

    cout << " First Number : " << a << endl ;

    cout << "Second Number : "  << b << endl;
    
    cout << "Arithmetic Operators : + - * / %" << endl;

    cout << " + " << (a+b) << endl;
    cout << " - " << (a-b) << endl;
    cout << " / " << (a/b) << endl;
    cout << " * " << (a*b) << endl;
    cout << " % " << (a%b) << endl;

    return 0;
}
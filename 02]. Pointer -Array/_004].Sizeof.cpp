#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int a = 10002 ;
    char c = 'A';
    double d = 1054102.2 ;

    cout << " int : " << a << " of size : " << sizeof(a) << endl;
    cout << " double : " << d << " of size : " << sizeof(d) << endl;
    cout << " char : " << c << " of size : " << sizeof(c) << endl;
    return 0;
}